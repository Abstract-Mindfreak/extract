function hasFlag(flag) {
  return process.argv.includes(flag);
}

function quoteIdentifier(value) {
  return `"${String(value || '').replace(/"/g, '""')}"`;
}

function hashText(value) {
  return crypto.createHash('md5').update(String(value || ''), 'utf8').digest('hex');
}

function buildConstraintName(tableName, constraintName) {
  const raw = `${tableName}_${constraintName}`.replace(/[^a-zA-Z0-9_]/g, '_');
  if (raw.length <= 63) return raw;
  return `${raw.slice(0, 50)}_${hashText(raw).slice(0, 12)}`;
}

function safeJson(value, fallback) {
  if (value == null) return fallback;
  if (typeof value === 'object') return value;
  try {
    return JSON.parse(value);
  } catch {
    return fallback;
  }
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

async function tableExists(databaseName, tableName) {
  const result = await getPool(databaseName).query(
    `SELECT to_regclass($1) IS NOT NULL AS exists`,
    [`public.${tableName}`],
  );
  return Boolean(result.rows[0]?.exists);
}

async function getRowCount(databaseName, tableName) {
  if (!(await tableExists(databaseName, tableName))) return null;
  const result = await getPool(databaseName).query(`SELECT COUNT(*)::bigint AS count FROM ${quoteIdentifier(tableName)}`);
  return Number(result.rows[0]?.count || 0);
}

async function loadSourceColumns(databaseName, tableName) {
  const result = await getPool(databaseName).query(`
    SELECT
      a.attnum,
      a.attname AS column_name,
      pg_catalog.format_type(a.atttypid, a.atttypmod) AS column_type,
      a.attnotnull AS not_null,
      a.attidentity AS identity_kind,
      pg_get_expr(d.adbin, d.adrelid) AS default_expr
    FROM pg_attribute a
    JOIN pg_class c ON c.oid = a.attrelid
    JOIN pg_namespace n ON n.oid = c.relnamespace
    LEFT JOIN pg_attrdef d ON d.adrelid = a.attrelid AND d.adnum = a.attnum
    WHERE n.nspname = 'public'
      AND c.relname = $1
      AND a.attnum > 0
      AND NOT a.attisdropped
    ORDER BY a.attnum
  `, [tableName]);

  return result.rows;
}

async function loadConstraintDefs(databaseName, tableName) {
  const result = await getPool(databaseName).query(`
    SELECT conname, contype, pg_get_constraintdef(oid, true) AS definition
    FROM pg_constraint
    WHERE conrelid = $1::regclass
      AND contype IN ('p', 'u')
    ORDER BY contype, conname
  `, [`public.${tableName}`]);
  return result.rows;
}

function buildColumnDefinition(column) {
  const parts = [quoteIdentifier(column.column_name), column.column_type];
  const defaultExpr = String(column.default_expr || '').trim();
  const identityKind = String(column.identity_kind || '').trim();

  if (identityKind === 'a') {
    parts.push('GENERATED ALWAYS AS IDENTITY');
  } else if (identityKind === 'd') {
    parts.push('GENERATED BY DEFAULT AS IDENTITY');
  } else if (/^nextval\(/i.test(defaultExpr) && /(smallint|integer|bigint)$/i.test(column.column_type)) {
    parts.push('GENERATED BY DEFAULT AS IDENTITY');
  } else if (defaultExpr) {
    parts.push(`DEFAULT ${defaultExpr}`);
  }

  if (column.not_null) {
    parts.push('NOT NULL');
  }

  return parts.join(' ');
}

async function ensureClonedTable(sourceDb, sourceTable, targetDb, targetTable, options = {}) {
  const columns = await loadSourceColumns(sourceDb, sourceTable);
  if (!columns.length) {
    throw new Error(`Source table ${sourceDb}.${sourceTable} has no readable columns`);
  }

  const targetPool = getPool(targetDb);
  const client = await targetPool.connect();
  try {
    await client.query('BEGIN');
    await client.query(`DROP TABLE IF EXISTS ${quoteIdentifier(targetTable)} CASCADE`);
    await client.query(`
      CREATE TABLE ${quoteIdentifier(targetTable)} (
        ${columns.map(buildColumnDefinition).join(',\n        ')}
      )
    `);

    if (!options.skipConstraints) {
      const constraints = await loadConstraintDefs(sourceDb, sourceTable);
      for (const constraint of constraints) {
        await client.query(`
          ALTER TABLE ${quoteIdentifier(targetTable)}
          ADD CONSTRAINT ${quoteIdentifier(buildConstraintName(targetTable, constraint.conname))} ${constraint.definition}
        `);
      }
    }

    await client.query('COMMIT');
  } catch (error) {
    await client.query('ROLLBACK');
    throw error;
  } finally {
    client.release();
  }

  return columns;
}

function prepareColumnValue(column, value) {
  if (value == null) return value;
  const columnType = String(column.column_type || '').toLowerCase();
  if (columnType === 'json' || columnType === 'jsonb') {
    return JSON.stringify(value);
  }
  return value;
}

async function copyTableData(sourceDb, sourceTable, targetDb, targetTable, columns) {
  const sourceRows = (await getPool(sourceDb).query(`SELECT * FROM ${quoteIdentifier(sourceTable)}`)).rows;
  const targetPool = getPool(targetDb);
  const client = await targetPool.connect();
  try {
    await client.query(`TRUNCATE TABLE ${quoteIdentifier(targetTable)} RESTART IDENTITY CASCADE`);
    if (!sourceRows.length) {
      return 0;
    }

    const batchSize = 250;
    const quotedColumns = columns.map((column) => quoteIdentifier(column.column_name)).join(', ');

    for (let start = 0; start < sourceRows.length; start += batchSize) {
      const batch = sourceRows.slice(start, start + batchSize);
      const values = [];
      const placeholders = [];
      let parameterIndex = 1;

      for (const row of batch) {
        placeholders.push(`(${columns.map(() => `$${parameterIndex++}`).join(', ')})`);
        for (const column of columns) {
          values.push(prepareColumnValue(column, row[column.column_name]));
        }
      }

      await client.query(`
        INSERT INTO ${quoteIdentifier(targetTable)} (${quotedColumns})
        VALUES ${placeholders.join(',\n')}
      `, values);
    }
  } finally {
    client.release();
  }

  return sourceRows.length;
}

async function cloneAndCopy(sourceDb, sourceTable, targetDb, targetTable, options = {}) {
  const columns = await ensureClonedTable(sourceDb, sourceTable, targetDb, targetTable, options);
  return copyTableData(sourceDb, sourceTable, targetDb, targetTable, columns);
}

async function copyIntoExistingTable(sourceDb, sourceTable, targetTable) {
  const sourceColumns = await loadSourceColumns(sourceDb, sourceTable);
  const targetColumns = await loadSourceColumns(TARGET_DB, targetTable);
  const targetColumnMap = new Map(targetColumns.map((column) => [column.column_name, column]));
  const sharedColumns = sourceColumns.filter((column) => targetColumnMap.has(column.column_name));
  const sourceRows = (await getPool(sourceDb).query(`SELECT * FROM ${quoteIdentifier(sourceTable)}`)).rows;
  const client = await getPool(TARGET_DB).connect();

  try {
    await client.query(`TRUNCATE TABLE ${quoteIdentifier(targetTable)} RESTART IDENTITY CASCADE`);
    if (!sourceRows.length || !sharedColumns.length) {
      return 0;
    }

    const batchSize = 250;
    const quotedColumns = sharedColumns.map((column) => quoteIdentifier(column.column_name)).join(', ');

    for (let start = 0; start < sourceRows.length; start += batchSize) {
      const batch = sourceRows.slice(start, start + batchSize);
      const values = [];
      const placeholders = [];
      let parameterIndex = 1;

      for (const row of batch) {
        placeholders.push(`(${sharedColumns.map(() => `$${parameterIndex++}`).join(', ')})`);
        for (const column of sharedColumns) {
          values.push(prepareColumnValue(column, row[column.column_name]));
        }
      }

      await client.query(`
        INSERT INTO ${quoteIdentifier(targetTable)} (${quotedColumns})
        VALUES ${placeholders.join(',\n')}
      `, values);
    }
  } finally {
    client.release();
  }

  return sourceRows.length;
}

