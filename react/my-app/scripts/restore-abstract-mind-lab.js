async function resetTableSequence(tableName, columnName = 'id') {
  await getPool(TARGET_DB).query(`
    SELECT setval(
      pg_get_serial_sequence($1, $2),
      COALESCE((SELECT MAX(${quoteIdentifier(columnName)}) FROM ${quoteIdentifier(tableName)}), 1),
      COALESCE((SELECT COUNT(*) > 0 FROM ${quoteIdentifier(tableName)}), FALSE)
    )
  `, [`public.${tableName}`, columnName]);
}

