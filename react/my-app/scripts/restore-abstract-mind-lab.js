function derivePromptTextFromChunk(row) {
  if (typeof row.prompt_text === 'string' && row.prompt_text.trim()) {
    return row.prompt_text.trim();
  }
  if (row.source_table !== 'tracks') return null;
  const payload = safeJson(row.chunk_text, null);
  const prompt = payload?.track?.prompt;
  return typeof prompt === 'string' && prompt.trim() ? prompt.trim() : null;
}

async function restoreRagChunksFromExport() {
  const exportPath = path.join(EXPORTED_DATA_DIR, 'rag_chunks.json');
  const items = JSON.parse(fs.readFileSync(exportPath, 'utf8'));
  await ensureRagChunksSchema();

  const pool = getPool(TARGET_DB);
  const client = await pool.connect();
  try {
    await client.query('BEGIN');
    await client.query('TRUNCATE TABLE rag_chunks');

    const batchSize = 100;
    for (let start = 0; start < items.length; start += batchSize) {
      const batch = items.slice(start, start + batchSize);
      const values = [];
      const placeholders = [];
      let index = 1;

      for (const row of batch) {
        placeholders.push(`($${index++}, $${index++}, $${index++}, $${index++}, $${index++}, $${index++}, $${index++}, $${index++}, $${index++}, $${index++}, $${index++}, $${index++})`);
        values.push(
          row.id,
          row.chunk_text,
          row.source_table,
          String(row.source_id),
          row.source_database,
          Array.isArray(row.tags) ? row.tags : [],
          row.category || null,
          row.domain || null,
          row.title || null,
          row.description || null,
          derivePromptTextFromChunk(row),
          hashText(row.chunk_text),
        );
      }

      await client.query(`
        INSERT INTO rag_chunks (
          id, chunk_text, source_table, source_id, source_database,
          tags, category, domain, title, description, prompt_text, chunk_hash
        )
        VALUES ${placeholders.join(',\n')}
      `, values);
    }

    await client.query('COMMIT');
  } catch (error) {
    await client.query('ROLLBACK');
    throw error;
  } finally {
    client.release();
  }

  return items.length;
}

